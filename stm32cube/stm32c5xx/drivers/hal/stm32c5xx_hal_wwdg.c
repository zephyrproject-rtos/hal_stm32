/** @defgroup WWDG_Introduction WWDG Introduction
  * @{

  The WWDG hardware abstraction layer provides a set of APIs to interface with the WWDG peripheral
  on STM32 microcontrollers.

  The system window watchdog (WWDG) is used to detect the occurrence of a software fault, usually generated by
  external interference or by unforeseen logical conditions, which causes the application program to abandon
  its normal sequence.

  The WWDG clock is prescaled from the APB clock and has a configurable time-window that can be programmed to detect
  abnormally late or early application behavior.\n

  An Early Wakeup Interrupt can be generated before a reset happens to perform a system recovery or manage certain
  actions before a system restart.

  Once the WWDG is enabled, it can only be disabled by a device reset.

  The WWDG is best suited for applications requiring the watchdog to react within an accurate timing window.

  This abstraction layer guarantees portability and ease of use across different STM32 series.
  */
